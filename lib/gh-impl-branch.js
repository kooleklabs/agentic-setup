const { spawnSync } = require('node:child_process');

function runGit(args, cwd) {
  const r = spawnSync('git', args, { encoding: 'utf8', cwd });
  if (r.error && r.error.code === 'ENOENT') {
    throw new Error('git not found on PATH.');
  }
  return r;
}

function requireZero(r, args) {
  if (r.status !== 0) {
    const stderr = (r.stderr || '').trim();
    throw new Error(`git ${args[0]} failed (exit ${r.status}): ${stderr || '(no stderr)'}`);
  }
  return r.stdout || '';
}

function createImplBranch({ cwd, slug, base }) {
  const branch = `impl/${slug}`;

  // 1. Working tree clean — empty porcelain output
  const status = runGit(['status', '--porcelain'], cwd);
  requireZero(status, ['status']);
  if ((status.stdout || '').trim().length > 0) {
    throw new Error(
      'Working tree has uncommitted changes. Commit or stash before running `--execute`.'
    );
  }

  // 2. Branch doesn't exist locally
  const localRef = runGit(['show-ref', '--verify', '--quiet', `refs/heads/${branch}`], cwd);
  if (localRef.status === 0) {
    throw new Error(
      `Branch ${branch} already exists locally. ` +
      `Delete it (\`git branch -D ${branch}\`) or pass \`--impl-branch <name>\` for a different name.`
    );
  }

  // 3. Branch doesn't exist remotely
  const remoteRef = runGit(['ls-remote', '--exit-code', '--heads', 'origin', branch], cwd);
  if (remoteRef.status === 0) {
    throw new Error(
      `Branch ${branch} already exists on origin. ` +
      `Delete it (\`git push origin :${branch}\`) or pass \`--impl-branch <name>\`.`
    );
  }

  // 4. Check out base
  const coBase = runGit(['checkout', base], cwd);
  requireZero(coBase, ['checkout']);

  // 5. Create new branch from base
  const coNew = runGit(['checkout', '-b', branch], cwd);
  requireZero(coNew, ['checkout']);

  return { branch };
}

function commitImplChanges({ cwd, slug, issueNumber, featureName }) {
  const addRes = runGit(['add', '-A'], cwd);
  requireZero(addRes, ['add']);

  // --quiet returns exit 0 if nothing staged, 1 if changes exist
  const diff = runGit(['diff', '--cached', '--quiet'], cwd);
  if (diff.status === 0) {
    return { sha: null };
  }

  const message = `wip(impl): ${featureName} (refs #${issueNumber})\n\nSlug: ${slug}\n\nGenerated by \`github-sync --execute\`. Review the diff, amend as needed, then open a PR.`;
  const commitRes = runGit(['commit', '-m', message], cwd);
  requireZero(commitRes, ['commit']);

  const revRes = runGit(['rev-parse', 'HEAD'], cwd);
  requireZero(revRes, ['rev-parse']);
  return { sha: (revRes.stdout || '').trim() };
}

function remoteBranchExists({ cwd, branch }) {
  const r = runGit(['ls-remote', '--exit-code', '--heads', 'origin', branch], cwd);
  return r.status === 0;
}

module.exports = { createImplBranch, commitImplChanges, remoteBranchExists };
